//Task2_26 В  массив  А(N)  вставить  максимальный  элемент  после  каждого  четного отрицательного элемента. 

$arr = array(-8, 11, 19, -4, 123, -88, 180, 99, 88, -6, 11);


$max = $arr[0];


for($k = 0; $k < count($arr); $k++) {
  
	if ($arr[$k] > $max ) {

  		$max = $arr[$k];

  	} 
	
}

for($i = 0; $i < count($arr); $i++){

	if($arr[$i] % 2 == 0 && $arr[$i] < 0){
		$arr[$i + 1] = $max;
	}

}
